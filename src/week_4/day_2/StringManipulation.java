package week_4.day_2;

public class StringManipulation {
    public static void main(String[] args) {
        String firstName = "Alexander Eric";
        System.out.println(firstName);

        //String Manipulation
        firstName = "Eric Alexander";
        System.out.println(firstName);
        //Bringing any changes to the value of your string is called String Manipulation.

        String fullName = "Alexander Eric";
        String message = "Welcome to TekSchool";
        System.out.println(fullName);

        System.out.println(fullName + " " + message);// It is called Concatenation
        //Combination of different pieces in to one is also called String Manipulation.
        //All type of changes that you bring in the value of you String is called manipulation.
        
    }
}
