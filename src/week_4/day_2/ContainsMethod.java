package week_4.day_2;

public class ContainsMethod {
    public static void main(String[] args) {
        //contains(): Return true or false if a specific string is available.
        String message = "welcome to TekSchool spartans students!";
        System.out.println(message.contains("spartans"));// true
        System.out.println(message.contains("ahmad")); // false
    }
}
