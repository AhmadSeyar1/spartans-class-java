package week_7.day_1;

public class EqualityAndNonEqualityOperators {
    public static void main(String[] args) {

        // Equality Operator

        int numberOne = 20;
        int numberTwo = 30;

        System.out.println(numberOne == numberTwo);//Result: false
        System.out.println(numberOne != numberTwo);//Result: true
        
    }
}
