package week_13.day_1;

public class FinalKeyWord {

    /*
    In Java the final keyword is used to declare that something cannot be changed
    The final keyword can be applied in various
    1. Final Variables
    2. Final Methods --> It cannot be overridden bay any subclass
    3. Final Classes --> It cannot be subclassed
     */

    public static void main(String[] args) {

        final int maxSpeed = 120;//this final variable value cannot be modified

    }
}
