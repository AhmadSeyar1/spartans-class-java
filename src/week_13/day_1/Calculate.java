package week_13.day_1;

public class Calculate {


    // Create a method to sum two int numbers and return the result
    // Create a method to sum three int numbers and return the result

    // Method Overloading
    public int sum(int numOne, int numTwo) {
        return numOne + numTwo;
    }

    public int sum(int numOne, int numTwo, int numThree) {
        return numOne + numTwo + numThree;
    }

    public double sum(double numOne, double numTwo) {
        return numOne + numTwo;
    }


}
