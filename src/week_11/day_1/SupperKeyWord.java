public class SupperKeyWord {

    /*
    Supper keyword:
    In Java, super keyword refers to the immediate parent of a class.
    It is used the following purposes:
    1. To refer to the parent lass instance variable: If child and parent classes have
    the same variables, and you need to refer to the parent class variable in the child
    class, you can use super.variableName.

    2. To invoke the parent class method: If your child class has overridden one of the
    parent class's methods, you can invoke the parent class method in the child class
    using super.methodName().

    3. To invoke the parent class constructor: super() is used to call the parent class's
    constructor from the child class. This is especially useful when setting up inheritance
    chains where each class might need some specific initialization.
     */


}
